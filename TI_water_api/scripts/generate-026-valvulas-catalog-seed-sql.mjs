#!/usr/bin/env node
/**
 * Regenerates scripts/migrations/026_seed_tiwater_valvulas_catalog.sql from catalog-seed/valvulas.
 * Run from TI_water_api: node scripts/generate-026-valvulas-catalog-seed-sql.mjs
 */
import { readFileSync, readdirSync, existsSync, writeFileSync } from 'fs';
import { join, dirname } from 'path';
import { fileURLToPath } from 'url';
import { buildPayload } from './import-catalog-helpers.mjs';

const __d = dirname(fileURLToPath(import.meta.url));
const apiRoot = join(__d, '..');
const seedRoot = join(apiRoot, 'catalog-seed/valvulas');
const outFile = join(apiRoot, 'scripts/migrations/026_seed_tiwater_valvulas_catalog.sql');

if (!existsSync(seedRoot)) {
  console.error('Missing', seedRoot);
  process.exit(1);
}

function toJsonbSql(obj) {
  const j = JSON.stringify(obj);
  return `'${j.replace(/\\/g, '\\\\').replace(/'/g, "''")}'::jsonb`;
}

function toTextSql(s) {
  if (s == null) return 'NULL';
  return `'${String(s).replace(/\\/g, '\\\\').replace(/'/g, "''")}'`;
}

const dirs = readdirSync(seedRoot, { withFileTypes: true })
  .filter((d) => d.isDirectory() && !d.name.startsWith('.'))
  .map((d) => d.name)
  .sort();

const rows = [];
for (const dir of dirs) {
  const p = join(seedRoot, dir, 'product.json');
  if (!existsSync(p)) continue;
  const data = JSON.parse(readFileSync(p, 'utf8'));
  const pl = buildPayload(data);
  rows.push({
    product_key: toTextSql(pl.productKey),
    code: toTextSql(pl.code),
    name: toTextSql(pl.name),
    description: toTextSql(pl.description),
    category: toTextSql(pl.category),
    price: pl.price == null ? 'NULL' : Number(pl.price),
    specifications: toJsonbSql(pl.specifications),
    images: toJsonbSql(pl.images),
    catalog_source: toTextSql(pl.catalogSource),
    page_number: pl.pageNumber == null ? 'NULL' : String(parseInt(pl.pageNumber, 10)),
    is_active: pl.isActive === false ? 'false' : 'true',
  });
}

if (rows.length === 0) {
  console.error('No product.json under', seedRoot);
  process.exit(1);
}

const header = `-- Seed válvulas catalog products (from catalog-seed/valvulas via buildPayload).
-- Regenerate: node scripts/generate-026-valvulas-catalog-seed-sql.mjs
-- Idempotent upsert by unique "code".
-- After this migration has run in production, do not edit this file; add a new
-- migration (e.g. 027_...) with a new filename if catalog data changes again.

`;

const valuesSql = rows
  .map(
    (r) => `  (${r.product_key}, ${r.code}, ${r.name}, ${r.description}, ${r.category}, ${r.price},
   ${r.specifications}, ${r.images}, ${r.catalog_source}, ${r.page_number}, ${r.is_active})`,
  )
  .join(',\n');

const sql =
  header +
  `INSERT INTO tiwater_products (
  product_key, code, name, description, category, price,
  specifications, images, catalog_source, page_number, is_active
) VALUES
${valuesSql}
ON CONFLICT (code) DO UPDATE SET
  product_key = EXCLUDED.product_key,
  name = EXCLUDED.name,
  description = EXCLUDED.description,
  category = EXCLUDED.category,
  price = EXCLUDED.price,
  specifications = EXCLUDED.specifications,
  images = EXCLUDED.images,
  catalog_source = EXCLUDED.catalog_source,
  page_number = EXCLUDED.page_number,
  is_active = EXCLUDED.is_active,
  updated_at = CURRENT_TIMESTAMP;

DO $$
BEGIN
  RAISE NOTICE '✅ 026_seed_tiwater_valvulas_catalog: % rows upserted', ${rows.length};
END $$;
`;

writeFileSync(outFile, sql, 'utf8');
console.log('Wrote', outFile, 'rows=', rows.length);
